const contractAddress = "0xa61972E9C76a519F42BBd9C7D27cb350c9357396";
const SEPOLIA_CHAIN_ID = 11155111;

let provider: BrowserProvider;
let contract: Contract;
let signer: any;

const initialize = async () => {
  if (!window.ethereum) {
    alert("Please install MetaMask.");
    throw new Error("MetaMask not detected");
  }

  provider = new BrowserProvider(window.ethereum);
  await window.ethereum.request({ method: "eth_requestAccounts" });

  const network = await provider.getNetwork();
  if (Number(network.chainId) !== SEPOLIA_CHAIN_ID) {
    try {
      await window.ethereum.request({
        method: "wallet_switchEthereumChain",
        params: [{ chainId: `0x${SEPOLIA_CHAIN_ID.toString(16)}` }],
      });
    } catch (error) {
      throw new Error("Please switch to the Sepolia network in MetaMask.");
    }
  }

  signer = await provider.getSigner();
  contract = new Contract(contractAddress, contractABI, signer);
};

const isConnected = () => !!provider && !!signer && !!contract;

const getContract = async () => {
  if (!isConnected()) {
    await initialize();
  }
  return contract;
};

const getProvider = async () => {
  if (!isConnected()) {
    await initialize();
  }
  return provider;
};

const getSigner = async () => {
  if (!isConnected()) {
    await initialize();
  }
  return signer;
};

const connectWallet = async () => {
  if (!isConnected()) {
    await initialize();
  }
  return signer.getAddress();
};

export { getContract, getProvider, getSigner, connectWallet, isConnected };